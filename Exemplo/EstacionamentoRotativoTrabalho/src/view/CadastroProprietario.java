    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        String nome = jTextFieldNome.getText();
        String tel = jTextFieldTelefone.getText();
        
        if("".equals(nome) || "".equals(tel)){ //código para a criação da caixa "Preencher todos os campos"
            Controladora.avisoPreencherTodosCampos();
        }else{
            model.Proprietario p = new model.Proprietario(nome, tel);
            CadastroVeiculo.addProprietario(p);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

